"""
dental_lexicon.py
=================
Лексикон для STT: подсказывает Whisper стоматологические термины и
правит самые частые ошибки распознавания.

Используется двумя способами:
1. `INITIAL_PROMPT` подаётся в Whisper как `initial_prompt=...`. Это «заставляет»
   модель ожидать именно такую лексику и резко поднимает точность на терминах.
2. `post_correct(text)` запускается ПОСЛЕ распознавания и заменяет частые ошибки.

Список ошибок собран из реального опыта работы Whisper с русской стоматологией:
- модель часто слышит «корица» вместо «кариес»
- путает «пульпит» с «полтит»/«пульс»
- режет окончания у длинных терминов
"""
from __future__ import annotations

import re


# ──────────────────────────────────────────────────────────────────────
# Список ключевых терминов и имён — подсказывается Whisper
# ──────────────────────────────────────────────────────────────────────
DENTAL_TERMS = [
    # болезни
    "кариес", "пульпит", "периодонтит", "пародонтит", "пародонтоз",
    "гингивит", "стоматит", "флюс", "абсцесс", "киста", "гранулёма",
    # процедуры
    "пломба", "пломбирование", "имплантация", "имплант", "коронка",
    "винир", "виниры", "брекеты", "элайнер", "элайнеры", "ретейнер",
    "отбеливание", "чистка", "гигиена", "ультразвуковая чистка",
    "Air Flow", "кюретаж", "удаление", "резекция", "синус-лифтинг",
    "протезирование", "съёмный протез", "вкладка", "терапия",
    "лечение каналов", "эндодонтия", "пульпотомия", "ампутация",
    "анестезия", "седация", "наркоз",
    # анатомия
    "зуб", "зубы", "клык", "резец", "моляр", "премоляр", "коренной",
    "молочный", "корень", "канал", "эмаль", "дентин", "десна", "челюсть",
    "верхняя челюсть", "нижняя челюсть", "прикус", "сустав",
    # специалисты
    "стоматолог", "терапевт", "хирург", "ортодонт", "ортопед",
    "пародонтолог", "имплантолог", "гигиенист", "ассистент", "детский стоматолог",
    # общее
    "осмотр", "консультация", "запись", "приём", "сегодня", "завтра",
    "понедельник", "вторник", "среда", "четверг", "пятница", "суббота", "воскресенье",
    "утром", "вечером", "днём",
]


# Список имён врачей клиники — подставляется из конфига при инициализации
DOCTOR_NAMES: list[str] = []


def build_initial_prompt(doctor_names: list[str] | None = None) -> str:
    """
    Собирает initial_prompt для Whisper из списка терминов и имён врачей.
    Длина ограничена ~200 токенами, чтобы не выжрать контекст.
    """
    names = doctor_names or DOCTOR_NAMES
    parts = [
        "Это разговор пациента с администратором стоматологической клиники.",
        "Возможные темы: " + ", ".join(DENTAL_TERMS[:30]) + ".",
    ]
    if names:
        parts.append("Имена врачей: " + ", ".join(names) + ".")
    parts.append("Пациент может назвать свой телефон, имя и удобное время.")
    return " ".join(parts)


# ──────────────────────────────────────────────────────────────────────
# Post-correction: замены частых ошибок распознавания
# ──────────────────────────────────────────────────────────────────────
# Формат: (regex_pattern, replacement, ignore_case)
POST_CORRECTIONS: list[tuple[str, str, bool]] = [
    # кариес
    (r"\bкорица\b", "кариес", True),
    (r"\bкориес\b", "кариес", True),
    (r"\bкаррис\b", "кариес", True),
    # пульпит
    (r"\bполпит\b", "пульпит", True),
    (r"\bполтит\b", "пульпит", True),
    (r"\bпульс\b(?=\s+(?:зуб|нерв))", "пульпит", True),
    # периодонтит
    (r"\bпериодон[ст]\b", "периодонтит", True),
    (r"\bпереодонтит\b", "периодонтит", True),
    # пародонтит
    (r"\bпарадонтит\b", "пародонтит", True),
    (r"\bпаро[нт]+ит\b", "пародонтит", True),
    # имплантация
    (r"\bимплантац+(?:и+|я)\b", "имплантация", True),
    (r"\bимплат\b", "имплант", True),
    # коронка
    (r"\bкаронка\b", "коронка", True),
    # ортодонт / ортопед
    (r"\bортодон\b", "ортодонт", True),
    (r"\bортопед\b(?=\w)", "ортопед", True),
    # ретейнер
    (r"\bретенер\b", "ретейнер", True),
    (r"\bретайнер\b", "ретейнер", True),
    # элайнер
    (r"\bэлайнор\b", "элайнер", True),
    (r"\bалайнер\b", "элайнер", True),
    # винир
    (r"\bвенир\b", "винир", True),
    (r"\bвенеер\b", "винир", True),
    # гигиена
    (r"\bгигена\b", "гигиена", True),
    # Air Flow
    (r"\bэр[\s\-]?флоу?\b", "Air Flow", True),
    (r"\bэйр[\s\-]?флоу?\b", "Air Flow", True),
    # частые опечатки в обычной речи
    (r"\bхачу\b", "хочу", True),
    (r"\bсколька\b", "сколько", True),
    (r"\bкакда\b", "когда", True),
    # «зап+ишите меня» → «запишите меня» (бывает галлюцинация с ударениями)
    (r"\+([аеёиоуыэюя])", r"\1", False),
]


def post_correct(text: str) -> str:
    """
    Прогоняет текст через словарь замен. Сохраняет регистр первой буквы
    исходного слова, если возможно.
    """
    if not text:
        return text
    out = text
    for pattern, replacement, ignore_case in POST_CORRECTIONS:
        flags = re.IGNORECASE if ignore_case else 0
        out = re.sub(pattern, replacement, out, flags=flags)
    return out


# ──────────────────────────────────────────────────────────────────────
# Полезный helper: грубая оценка, выглядит ли текст «стоматологическим»
# ──────────────────────────────────────────────────────────────────────
_DENTAL_HINT_WORDS = {
    "зуб", "зубы", "зуба", "зубов", "десна", "десны", "пломба", "коронка",
    "кариес", "пульпит", "брекет", "имплант", "осмотр", "стоматолог",
    "болит", "ноет", "флюс", "опух", "опухло", "запись", "записать",
    "приём", "прием", "врач", "клиника",
}


def looks_dental(text: str) -> bool:
    """True если в тексте есть >=1 стоматологическое слово."""
    if not text:
        return False
    words = re.findall(r"[А-Яа-яЁё]+", text.lower())
    return any(w in _DENTAL_HINT_WORDS or any(w.startswith(h[:5]) for h in _DENTAL_HINT_WORDS) for w in words)


