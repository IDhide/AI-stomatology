def lookup_faq(text: str) -> str | None:
    """Возвращает готовый ответ, если вопрос совпал с FAQ, иначе None."""
    if not text:
        return None
    t = text.lower()
    for item in FAQ:
        for pat in item.triggers:
            if re.search(pat, t):
                return item.answer
    return None


if __name__ == "__main__":
    samples = [
        "Можно в рассрочку?",
        "Делаете снимки?",
        "У вас есть парковка?",
        "Я беременна, можно лечить?",
    ]
    for s in samples:
        print(f"{s!r:35} → {lookup_faq(s)}")
