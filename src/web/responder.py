"""
responder.py
============
Лёгкий «мозг» голосового ассистента Оливии для веб-демо — без Ollama.

Отвечает на реальную речь пациента (после распознавания в браузере) по данным
персоны «Стоматология №1»: цены под ключ, акции, бесплатная консультация,
направление на удаление к партнёру-хирургу, отказ по детям, запись (имя/время/
телефон, время согласует администратор).

Это правиловый ответчик для демонстрации голосового цикла. Когда поднят Ollama,
боевой мозг — src/llm/assistant.py с тем же промптом config/prompts.yaml.
"""
from __future__ import annotations

import re

NAME = "Оливия"
CLINIC = "Стоматология номер один"


def _norm(text: str) -> str:
    t = text.lower().replace("ё", "е")
    return re.sub(r"\s+", " ", t).strip()


def _has(t: str, *words: str) -> bool:
    return any(w in t for w in words)


def reply(user_text: str) -> str:
    """Вернуть голосовую реплику Оливии на реплику пациента."""
    t = _norm(user_text)
    if not t:
        return "Простите, я вас не расслышала. Повторите, пожалуйста?"

    # 1) Острая боль / неотложное — приоритет
    if _has(t, "боль", "болит", "ноет", "флюс", "опухл", "распухл", "температур",
            "кровотеч", "травм", "выбил", "сломал зуб", "очень больно"):
        return ("Понимаю, это нужно посмотреть как можно скорее. До приёма не "
                "грейте больное место и можно принять обычное обезболивающее. "
                "Записать вас на ближайшую бесплатную консультацию?")

    # 2) Дети — не лечим
    if _has(t, "ребен", "ребён", "дет", "дочк", "сын", "малыш", "годик", "годa", "лет ребен"):
        return ("К сожалению, детей мы не лечим — детской стоматологии у нас нет. "
                "Подсказать что-нибудь по взрослому приёму?")

    # 3) Удаление — к партнёру-хирургу
    if _has(t, "удал", "вырв", "выдерн", "удаление зуба"):
        return ("Удалением занимается наш партнёр-хирург. Запишите его номер: "
                "восемь, девятьсот двадцать один, четыреста сорок четыре, "
                "пятьдесят, семьдесят. Скажите, что вы от нас, от «Стоматологии "
                "номер один», тогда он сделает скидку. Подсказать что-то ещё?")

    # 4) Конкретные процедуры и цены (всё под ключ)
    if _has(t, "винир"):
        if _has(t, "композит"):
            return ("Композитный винир — семь тысяч рублей. Это временный вариант "
                    "на два-три года. Хотите записаться на бесплатную консультацию, "
                    "чтобы врач подобрал оптимальный вариант?")
        return ("Сейчас у нас акция на виниры E-Max — под ключ от двадцати восьми с "
                "половиной до тридцати пяти тысяч рублей, входят все манипуляции, "
                "сканирование и работа врача. Точную стоимость врач назовёт на "
                "бесплатной консультации. Записать вас?")

    if _has(t, "коронк"):
        if _has(t, "имплант"):
            return ("Циркониевая коронка на имплант сейчас по акции — тридцать шесть "
                    "тысяч рублей под ключ. Подобрать вам время для бесплатной "
                    "консультации?")
        return ("Циркониевая коронка — тридцать тысяч рублей под ключ, сканирование "
                "входит в стоимость. Хотите записаться на бесплатную консультацию?")

    if _has(t, "чистк", "гигиен", "камень", "налет"):
        return ("Профессиональная чистка зубов — пять тысяч рублей. Записать вас на "
                "удобное время?")

    if _has(t, "канал", "пульпит", "нерв"):
        return ("Лечение одного канала — шесть тысяч рублей. Точный план врач "
                "составит на бесплатной консультации. Подобрать вам время?")

    if _has(t, "пломб", "кариес", "дырк", "лечение зуб"):
        return ("Лечение с пломбой — от четырёх с половиной до семи тысяч рублей, "
                "цена зависит от объёма. Записать вас на бесплатный осмотр?")

    if _has(t, "вкладк"):
        return ("Вкладка — шесть тысяч триста рублей, её ставят в корень зуба по "
                "показанию врача. Хотите записаться на консультацию?")

    if _has(t, "имплант"):
        return ("Стоимость имплантации врач рассчитает индивидуально на бесплатной "
                "консультации. А коронка на имплант сейчас по акции — тридцать шесть "
                "тысяч. Подобрать вам время?")

    if _has(t, "протез", "акри", "бюгель", "съемн", "съёмн"):
        return ("По протезированию под ключ: Акри-Фри — сорок три тысячи за челюсть, "
                "акриловый протез — двадцать восемь тысяч за челюсть, бюгельный — от "
                "сорока пяти до пятидесяти пяти тысяч. Какой вариант вам ближе, "
                "записать на консультацию?")

    if _has(t, "отбел"):
        return ("По отбеливанию точную программу врач подберёт на бесплатной "
                "консультации, чтобы это было безопасно для эмали. Записать вас?")

    # 5) Мета-вопросы
    if _has(t, "адрес", "где наход", "как добрат", "куда ехать", "где вы"):
        return ("Мы находимся на улице Ивана Фомина, дом четырнадцать корпус один, "
                "второй этаж, в Санкт-Петербурге. Записать вас на удобное время?")

    if _has(t, "телефон", "номер клиник", "позвонить", "как связат"):
        return ("Наш телефон: восемь, девятьсот одиннадцать, девятьсот двадцать "
                "шесть, тридцать два, пятнадцать. Хотите, я подберу вам время прямо "
                "сейчас?")

    if _has(t, "час", "когда работа", "режим работ", "во сколько", "график"):
        return ("Мы работаем ежедневно с двенадцати до двадцати одного. Подобрать "
                "вам удобный день для консультации?")

    if _has(t, "скольк", "стоимост", "цена", "цены", "прайс", "по чем"):
        return ("Каждый случай индивидуален, точную стоимость врач назовёт после "
                "осмотра, а консультация у нас бесплатная. Подскажите, что именно "
                "вас интересует — лечение, протезирование или эстетика?")

    # 6) Запись / контактные данные
    if re.search(r"\b\d[\d\s\-]{5,}", t) or _has(t, "меня зовут", "запиш", "продикт"):
        return ("Спасибо, записала. Администратор перезвонит и согласует точное "
                "время консультации. Остались ещё вопросы?")

    if _has(t, "запис", "хочу на прием", "хочу на приём", "можно записат", "свободн", "окно", "когда можно"):
        return ("Конечно, подберём удобное время. Назовите, пожалуйста, ваше имя и "
                "продиктуйте номер телефона — администратор перезвонит и согласует "
                "запись. На какой день вам удобнее?")

    # 7) Социальное
    if _has(t, "спасиб", "благодар"):
        return "Пожалуйста, всегда рада помочь. Записать вас на консультацию?"

    if _has(t, "до свид", "пока", "всего добр", "досвид"):
        return ("Спасибо, что обратились в «Стоматологию номер один». Хорошего дня! "
                "Если что — звоните, будем рады помочь.")

    if _has(t, "здравств", "добрый", "привет", "алло"):
        return ("Здравствуйте! Подскажите, что вас беспокоит, и я помогу подобрать "
                "решение?")

    # 8) По умолчанию — мягко ведём к консультации
    return ("Поняла вас. Лучше всего, если врач посмотрит и составит план на "
            "бесплатной консультации. Подобрать вам удобное время?")


def greeting() -> str:
    return ("Здравствуйте, меня зовут Оливия, я администратор клиники "
            "«Стоматология номер один». Подскажите, что вас беспокоит?")
