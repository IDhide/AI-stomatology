def is_relevant(text: str) -> bool:
    """
    True, если реплика похожа на обращение по теме клиники. Фоновую болтовню
    (без стоматологических/сервисных слов) отсекаем, чтобы Оливия не отвечала
    на случайные разговоры рядом с экраном.
    """
    t = _norm(text)
    if not t:
        return False
    words = [w for w in re.split(r"[^a-zа-я0-9]+", t) if w]
    # короткий чистый бэкчэннел («понял», «ну так») — игнорируем
    if len(words) <= 3 and all(w in _BACKCHANNEL for w in words):
        return False
    # явно посторонний разговор (телефон, спутник, ребёнок) — игнорируем
    if _has(t, *_OFFTOPIC_PHRASES) and not _has(t, *_TOPIC):
        return False
    # характерные основы по теме
    if _has(t, *_TOPIC):
        return True
    # короткое да/нет/ок как ответ в диалоге
    if set(words) & _SHORT_YES_NO:
        return True
    return False


def wants_slots(text: str) -> bool:
    """Пациент просит подобрать время / спрашивает про окна — нужен DIKIDI."""
    t = _norm(text)
    return _has(t, "свободн", "окошк", "окно", "подбер",
                "ближайш", "когда можно", "когда есть", "на завтра",
                "на недел", "удобное время", "какое время", "хочу записат",
                "можно записат", "запишите меня")


# Фразы «я пришёл по записи» — для проверки записи на ресепшене.
_CHECKIN = ("у меня запись", "я по записи", "пришел по записи", "пришёл по записи",
            "пришла по записи", "записан на", "записана на", "мне назначен",
            "мне назначено", "у меня прием", "у меня приём", "меня записали")


def wants_checkin(text: str) -> bool:
    """Пациент пришёл по записи — нужно проверить запись."""
    t = _norm(text)
    return _has(t, *_CHECKIN)


def extract_time(text: str):
    """Достаёт время записи из речи: «на 16:00», «на 16», «на шестнадцать»."""
    t = _norm(text)
    m = re.search(r"\b(\d{1,2})[:\.\s]?(\d{2})?\b", t)
    if m:
        hh = int(m.group(1))
        mm = m.group(2) or "00"
        if 0 <= hh <= 23:
            return f"{hh:02d}:{mm}"
    words = {
        "десять": 10, "одиннадцать": 11, "двенадцать": 12, "тринадцать": 13,
        "четырнадцать": 14, "пятнадцать": 15, "шестнадцать": 16,
        "семнадцать": 17, "восемнадцать": 18, "девятнадцать": 19, "двадцать": 20,
    }
    for w, hh in words.items():
        if w in t:
            return f"{hh:02d}:00"
    return None


def specialty_for(text: str) -> str:
    """К какому специалисту искать окно (для запроса в DIKIDI)."""
    t = _norm(text)
    if _has(t, "чистк", "гигиен", "камень", "налет"):
        return "гигиенист"
    if _has(t, "брекет", "элайнер", "прикус", "ортодонт", "выравн"):
        return "ортодонт"
    if _has(t, "имплант"):
        return "имплантолог"
    if _has(t, "коронк", "винир", "протез", "вкладк", "акри", "цирконий"):
        return "ортопед"
    return "терапевт"


def reply(user_text: str) -> str:
    """Вернуть голосовую реплику Оливии на реплику пациента."""
    t = _norm(user_text)
    if not t:
        return "Простите, я вас не расслышала. Повторите, пожалуйста?"

    # 0) Неэтичные темы — мягко возвращаем к делу
    if _has(t, "политик", "путин", "выбор", "религи", "бог ", "церков", "национальн",
            "нация", "секс", "порно", "наркотик"):
        return ("Давайте лучше я помогу вам по записи или подскажу что-нибудь по "
                "клинике.")

    # 1) Туалет и навигация по клинике
    if _has(t, "туалет", "уборн", "где помыт руки", "помыть руки"):
        return "Туалет прямо по коридору и налево."
    if _has(t, "гардероб", "раздева", "куда верхн", "куда куртк", "повесить"):
        return "Гардероб слева от входа, повесьте, пожалуйста, там."
    if _has(t, "бахил"):
        return "Бахилы у входа, наденьте, пожалуйста, и проходите."
    if _has(t, "где сесть", "где подожда", "куда присес", "зона ожидан", "где ожидат"):
        return "Присаживайтесь в зоне ожидания справа, врач вас пригласит."

    # 2) Острая боль — без советов по лечению, к врачу
    if _has(t, "боль", "болит", "ноет", "флюс", "опухл", "распухл", "температур",
            "кровотеч", "травм", "выбил", "сломал зуб", "очень больно"):
        return ("Поняла, это лучше показать врачу. Подскажите, вы по записи? "
                "Тогда присаживайтесь, я предупрежу врача.")

    # 3) Дети — не лечим
    if _has(t, "ребен", "ребён", "дет", "дочк", "сын", "малыш", "годик", "лет ребен"):
        return "К сожалению, детей мы не лечим — детской стоматологии у нас нет."

    # 4) Удаление — к партнёру-хирургу
    if _has(t, "удал", "вырв", "выдерн", "удаление зуба"):
        return ("Удалением занимается наш партнёр-хирург. Его номер: восемь, "
                "девятьсот двадцать один, четыреста сорок четыре, пятьдесят, "
                "семьдесят. Скажите, что вы от «Стоматологии номер один».")

    # 5) Цены — отвечаем прямо, без навязывания записи
    if _has(t, "винир"):
        if _has(t, "композит"):
            return "Композитный винир — семь тысяч рублей."
        return ("Виниры E-Max сейчас по акции — от двадцати восьми с половиной до "
                "тридцати пяти тысяч рублей под ключ.")
    if _has(t, "коронк"):
        if _has(t, "имплант"):
            return "Циркониевая коронка на имплант — тридцать шесть тысяч рублей, акция."
        return "Циркониевая коронка — тридцать тысяч рублей под ключ."
    if _has(t, "чистк", "гигиен", "камень", "налет"):
        return "Профессиональная чистка зубов — пять тысяч рублей."
    if _has(t, "канал", "пульпит", "нерв"):
        return "Лечение одного канала — шесть тысяч рублей."
    if _has(t, "пломб", "кариес", "дырк", "лечение зуб"):
        return "Лечение с пломбой — от четырёх с половиной до семи тысяч рублей."
    if _has(t, "вкладк"):
        return "Вкладка — шесть тысяч триста рублей."
    if _has(t, "имплант"):
        return ("Стоимость имплантации врач рассчитает на приёме. Коронка на имплант "
                "сейчас по акции — тридцать шесть тысяч.")
    if _has(t, "протез", "акри", "бюгель", "съемн", "съёмн"):
        return ("Акри-Фри — сорок три тысячи за челюсть, акриловый протез — двадцать "
                "восемь тысяч, бюгельный — от сорока пяти до пятидесяти пяти тысяч.")
    if _has(t, "отбел"):
        return "По отбеливанию программу подберёт врач на приёме."

    # 6) Мета-вопросы
    if _has(t, "адрес", "где наход", "как добрат", "куда ехать", "где вы"):
        return ("Мы на улице Ивана Фомина, дом четырнадцать корпус один, второй этаж.")
    if _has(t, "телефон", "номер клиник", "позвонить", "как связат"):
        return ("Наш телефон: восемь, девятьсот одиннадцать, девятьсот двадцать "
                "шесть, тридцать два, пятнадцать.")
    if _has(t, "час", "когда работа", "режим работ", "во сколько работ", "график"):
        return "Мы работаем ежедневно с двенадцати до двадцати одного."

    if _has(t, "скольк", "стоимост", "цена", "цены", "прайс", "по чем"):
        return ("Подскажите, какая услуга вас интересует — назову стоимость.")

    # 7) Социальное
    if _has(t, "спасиб", "благодар"):
        return "Пожалуйста!"
    if _has(t, "до свид", "пока", "всего добр", "досвид"):
        return "Хорошего дня!"
    if _has(t, "здравств", "добрый", "привет", "алло"):
        return "Здравствуйте! Чем могу помочь?"

    # 8) По умолчанию — коротко, без навязывания
    return ("Подскажите, вы по записи или с вопросом по клинике?")


def greeting() -> str:
    return "Здравствуйте! Меня зовут Оливия. Чем могу вам помочь?"
